    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:String username = jTextField1.getText();
        String description = jTextArea1.getText();
        String amountStr = jTextField1.getText();

        
        int selectedIndex = jComboBox1.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this,"Please enter valid covid charity");
            return;
        }
        if(description.isEmpty() || amountStr.isEmpty()){
              JOptionPane.showMessageDialog(this,"All fields are mandatory");
              return;
 
        }
        int amount =0;
        try {
            amount = Integer.parseInt(amountStr);
            if (amount <= 0) {
                JOptionPane.showMessageDialog(this, "Amount cannot be negative or 0");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Amount should be number");
            return;
        }
        


        
        String ccName = (String) jComboBox1.getSelectedItem();
        CovidCharity ccSelected = null;
        
        for (CovidCharity cc: this.covidVaccinationSystem.getCovidCharityDirectory().getCovidCharities()) {
            if (cc.getName().equals(ccName)) {
                ccSelected = cc;
                break;
            }
        }
        
        if (ccSelected == null) {
            JOptionPane.showMessageDialog(this,"Please enter valid covid charity");
            return;
        }
                
        FundsRequest fundsRequest = new FundsRequest(description, amount, ccSelected);
        
        medicalCenter.addFundsRequest(fundsRequest);

        populateTable();

        jTextArea1.setText("");
        jTextField1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

