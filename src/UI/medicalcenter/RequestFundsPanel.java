    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (FundsRequest fr : this.medicalCenter.getFundsRequests()) {
            Object[] row = new Object[4];
            row[0] = fr;                     
            row[1] = fr.getAmount();
            row[2] = fr.getCovidCharity().getName();
            row[3] = fr.getStatus();
            model.addRow(row);
        }
        
        dbConnector.storeSystem(covidVaccinationSystem);

    }

